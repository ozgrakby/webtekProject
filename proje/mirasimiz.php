            <article class="row">
                <p>&nbsp;&nbsp;&nbsp;&nbsp; Antik kent, Frigya'ya bağlı yaşayan Aizanitis'lerin ana yerleşim merkeziydi. Kentin yüksek platosu üzerinde bulunan Zeus Tapınağı'nın çevresinde yapılan kazılarda İlk Tunç Çağına dayanan yerleşim izleri ortaya çıkmıştır.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; Bu bölge MÖ 133'de Roma egemnliğine girmiştir. <b>Aizanoi</b> Roma döneminde tahıl ekimi, yün ve şarap üretimi sayesinde zenginleşmiş ve ünlenmiştir. Bu bölgede ilk sikkelerin MÖ 1. yüzyılın sonlarında basıldığı bilinmektedir. <b>Aizanoi</b>, en parlak dönemini MS 2. yüzyılda yaşamıştır. Bu dönemde büyük imar faaliyetleri dörmüştür.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; Erken Bizans Dönemi'nde <b>Aizanoi</b>, psikoposluk merkezi iken, MS 7. yüzyıldan sonra önemini yitirmiştir. Tapınak düzlüğü Orta Çağ'da bir hisara dönüştürülmüştür. Selçuklu egemnliğinde ise <b>Aizanoi</b>, Çavdar Tatarları tarafından üs olarak kullanılmasından dolayı buraya Çavdarhisar adı verilmiştir.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Aizanoi</b>, 19. yüzyılın ilk çeyreğinde Avrupalı gezginler tarafından keşfedilmiş, daha sonra incelenmiş ve tanımlanmıştır. 1926 yılında yapılan ilk kazılar sonucu yapılan çalışmalar Alman arkeolog Dr. Daniel Krencker tarafından yayınlanmıştır. 1970'den bu yana kazı çalışmaları devam etmektedir.</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Aizanoi</b> antik kentinde; Zeuz tapınağı, tiyatro, stadyum, iki hamam, dünyanın ilk ticaret borsa binası, sütunlu cadde, Kocaçay üzerinde beş köprü -ikisi ayaktadır-, iki agora, nekropoller, antik bir bent ve su yolları bulunmaktadır.</p>
