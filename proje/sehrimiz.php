                <p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Kütahya</b>'nın kuruluş tarihi kesin olmamakla birlikte İlk Tunç Çağına kadar dayanmaktadır. Bu topraklara yerleşen en eski topluluk Friglerdir. Kütahya'da Frig varlığı MÖ 1200'lere dayanmaktadır. MÖ 676 yılında Kimmerler, Frigleri mağlup ederek <b>Kütahya</b> ve Çervresine egemen oldular. Daha sonra Lidyalılara geçen <b>Kütahya</b>, MÖ 546 yılında Perslerin Anadolu'yu istila etmesi sonucu el değiştirmişti. MÖ 334 yılında İskender'in Persleri yenmesi ile yöreyi egemenlik altına aldı. MÖ 133 yılında Roma yönetimine giren <b>Kütahya</b> daha sonra Piskoposluk merkezi haline gelmiştir.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; 1071 Malazgirt Meydan muharebesi sonrası Alparslan'a yenilen Diyojen esirliğin sonra <b>Kütahya</b>'ya getirilip gözleri kör edilmiştir. 1078'de Anadolu Selçuklu Devletinin kurucusu Kutalmışoglu Süleyman Şah <b>Kütahya</b>'yı ele geçirmiştir. Daha sonra şehir Bizans, Haçlılar ve Selçuklu Devleti arasında el değiştirip durmuştur. 1277'den sonra şehir Germiyanoğlu Beyliğinin egemenliğine girmiştir. 1381'de gerçekleşen Yıldırım Bayezid ile Devlet Hatun evliliği sonucu Kütahya ve çevresi Osmalılara çeyiz olarak verilmiştir. 1402 Ankara savaşından sonra Anadolu Timur tarafından istila edildiğinde Kütahya II. Yakup Beye verilmiş ve daha sonra şehir geri Osmalılara geçmiştir.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Kütahya</b>'da çini örnekleri 14.yüzyıl sonlarında görülmeye başlanmıştır. Çinide asıl ilerleme İznik'in çini sanatının zirvesinde olduğu 1550'li yıllardan sonra başlamıştır. 18. yüzyılda İznik'te çiniciliğin tamamen kaybolmasından sonra Osmalı'da tek yer <b>Kütahya</b> kalmıştır.</p><br>
                <p>&nbsp;&nbsp;&nbsp;&nbsp; <b>Kütahya</b>'nın yemek kültürünün temelini buğday ürünleri, hamurlular ve süt ürünleri oluşturur. Hamurlara örnek olarak; cimcik, kıymalı-mercimekli mantı, tirit, haşhaşlı pide, dolamber böreği, şibit gösterilebilir. Sıkıcık -tarhana ve bulgur küçük yuvarlak köftelerle yapılır-, düğün çorbası, tutmaç, tarhana, paça çorbası çorbalara örnek göstirelebilir. Labada dolması, lahana sarması, soğan dolması, göbeç(güveç) yine <b>Kütahya</b>'nın yemeklerindendir.</p><br>
